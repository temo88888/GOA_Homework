def calculator (num1 , num2, opr):
    if opr == "+":
        return num1 + num2
    elif opr == "-":
        return num1 - num2
    elif opr == "*":
        return num1  *num2
    elif opr == "/":
        return num1 / num2
calculator(int(input("Enter first number"), int(input("Enter second number"), input("Enter Operator"))))